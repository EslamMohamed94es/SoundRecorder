package com.example.soundrecorder;


public class RecordingItems {
    public String RecorderName;
    public String RecorderDuration;
    public String RecorderDate;

    public RecordingItems(String recorderName, String recorderDuration, String recorderDate) {
        RecorderName = recorderName;
        RecorderDuration = recorderDuration;
        RecorderDate = recorderDate;
    }
}
